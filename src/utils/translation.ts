export function getDictionaryLanguage(countryCode: string, langCode: string): string | null {
  const lc = langCode.toLowerCase().split('-')[0];
  if (lc === 'fr') return 'FR';
  if (lc === 'en') return 'EN';
  if (lc === 'es') return 'ES';
  if (lc === 'de') return 'DE';
  if (lc === 'it') return 'IT';
  if (lc === 'pt') return 'PT';
  if (lc === 'ar') return 'AR';
  
  // Direct matching checks
  if (countryCode === 'FR') return 'FR';
  if (countryCode === 'US' || countryCode === 'GB' || countryCode === 'AU' || countryCode === 'CA') return 'EN';
  if (countryCode === 'ES' || countryCode === 'MX' || countryCode === 'AR' || countryCode === 'CO') return 'ES';
  if (countryCode === 'DE' || countryCode === 'AT') return 'DE';
  if (countryCode === 'IT') return 'IT';
  if (countryCode === 'PT' || countryCode === 'BR') return 'PT';
  if (countryCode === 'SA' || countryCode === 'AE' || countryCode === 'QA') return 'AR';

  return null;
}

export function getTranslation(key: string, countryCode: string): string {
  const country = countries.find(c => c.code === countryCode);
  const langCode = country ? country.langCode : 'fr';
  const dictLang = getDictionaryLanguage(countryCode, langCode);

  if (dictLang) {
    return translations[dictLang]?.[key] || translations['FR']?.[key] || key;
  }
  
  // Fallback to French if no native translation is found. Google Translate will then translate from French to the target lang in-browser.
  return translations['FR']?.[key] || key;
}

export function useTranslation() {
  const [lang, setLang] = useState<Language>(() => {
    return localStorage.getItem('app_country') || 'FR';
  });

  const changeLanguage = (countryCode: Language) => {
    const country = countries.find(c => c.code === countryCode);
    if (country) {
      localStorage.setItem('app_country', country.code);
      const dictLang = getDictionaryLanguage(country.code, country.langCode);
      if (dictLang) {
        // Native dictionary covers this! Google Translate should remain in "fr" (original)
        localStorage.setItem('app_language', 'fr');
      } else {
        // Google Translate needs to translate from French "fr" to the target country's langCode
        localStorage.setItem('app_language', country.langCode);
      }
      setLang(country.code);
    } else {
      localStorage.setItem('app_country', countryCode);
      localStorage.setItem('app_language', 'fr');
      setLang(countryCode);
    }
    window.dispatchEvent(new Event('app_language_change'));
  };

  useEffect(() => {
    const handleLangChange = () => {
      const currentCountry = localStorage.getItem('app_country') || 'FR';
      setLang(currentCountry);
    };
    window.addEventListener('app_language_change', handleLangChange);
    return () => {
      window.removeEventListener('app_language_change', handleLangChange);
    };
  }, []);

  const t = (key: string): string => {
    return getTranslation(key, lang);
  };

  return { t, lang, changeLanguage };
}
